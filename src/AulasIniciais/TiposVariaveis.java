package AulasIniciais;


public class TiposVariaveis {
    public static void main(String[] args) {
        // Os oito tipos primitivos em java são int, byte, short, long, float, double, boolean e char
        // São armazenados na pilha de memória
        // Variavel é um espaço na memória para armazenar valores

        int a = 2;

        System.out.println(a);

        a = 4;

        System.out.println(a);

        final float teste = 45F;

        System.out.println(teste);

        // *ERRADO* teste = 2;


    }
}
